def addition(int1=10, int2=200) -> int:
    return int1 + int2


def subtraction(int1=2, int2=1) -> int:
    return int1 - int2


def multiplication(int1=2, int2=1) -> int:
    return int1 * int2


def division(int1=2, int2=1):
    return int1 / int2


print(addition())
print(subtraction())
print(multiplication(2, 10))
print(division())
